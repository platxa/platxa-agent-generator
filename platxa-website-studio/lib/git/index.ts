/**
 * Git service for theme version control
 */
export class GitService {
  private repo: InMemoryGitRepo;

  constructor(config?: Partial<GitConfig>) {
    this.repo = new InMemoryGitRepo(config);
  }

  /**
   * Initialize a new repository with theme files
   */
  initRepository(files: GeneratedFile[]): GitOperationResult {
    return this.repo.init(files);
  }

  /**
   * Save changes with a commit
   */
  saveChanges(
    files: GeneratedFile[],
    message: string
  ): GitOperationResult {
    // Update files
    for (const file of files) {
      this.repo.updateFile(file.path, file.content);
    }

    // Stage all changes
    this.repo.add("all");

    // Commit
    return this.repo.commit(message);
  }

  /**
   * Get current status
   */
  getStatus(): GitStatus {
    return this.repo.status();
  }

  /**
   * Get commit history
   */
  getHistory(limit?: number): GitCommit[] {
    return this.repo.log(limit);
  }

  /**
   * Create a new branch
   */
  createBranch(name: string, checkout?: boolean): GitOperationResult {
    return this.repo.branch(name, checkout);
  }

  /**
   * Switch branches
   */
  switchBranch(name: string): GitOperationResult {
    return this.repo.checkout(name);
  }

  /**
   * List all branches
   */
  getBranches(): GitBranch[] {
    return this.repo.listBranches();
  }

  /**
   * Get all files in repository
   */
  getFiles(): GeneratedFile[] {
    const files: GeneratedFile[] = [];
    this.repo.getFiles().forEach((content, path) => {
      const ext = path.split(".").pop() || "";
      const typeMap: Record<string, GeneratedFile["type"]> = {
        xml: "xml",
        py: "py",
        scss: "scss",
        css: "css",
        js: "js",
        po: "po",
        pot: "pot",
      };
      files.push({
        path,
        content,
        type: typeMap[ext] || "xml",
      });
    });
    return files;
  }

  /**
   * Export repository state for storage
   */
  exportState(): ReturnType<InMemoryGitRepo["export"]> {
    return this.repo.export();
  }

  /**
   * Import repository state from storage
   */
  importState(state: ReturnType<InMemoryGitRepo["export"]>): void {
    this.repo.import(state);
  }
}

