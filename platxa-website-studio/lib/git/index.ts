/**
 * Git Integration for Theme Version Control
 *
 * Provides Git operations for Odoo themes including:
 * - Repository initialization
 * - Commit history management
 * - Branch operations
 * - Remote repository sync
 */

import type { GeneratedFile } from "../odoo-skills";

// =============================================================================
// TYPES
// =============================================================================

export interface GitConfig {
  /** Repository URL (GitHub, GitLab, etc.) */
  remoteUrl?: string;
  /** Default branch name */
  defaultBranch: string;
  /** Author name for commits */
  authorName: string;
  /** Author email for commits */
  authorEmail: string;
}

export interface GitCommit {
  /** Commit hash (SHA) */
  hash: string;
  /** Short hash (7 chars) */
  shortHash: string;
  /** Commit message */
  message: string;
  /** Author name */
  author: string;
  /** Author email */
  email: string;
  /** Commit timestamp */
  timestamp: Date;
  /** Files changed in this commit */
  files: string[];
}

export interface GitBranch {
  /** Branch name */
  name: string;
  /** Is current branch */
  current: boolean;
  /** Last commit on branch */
  lastCommit?: GitCommit;
  /** Is remote tracking branch */
  isRemote: boolean;
}

export interface GitStatus {
  /** Current branch */
  branch: string;
  /** Is repository clean */
  clean: boolean;
  /** Modified files */
  modified: string[];
  /** Added files (staged) */
  staged: string[];
  /** Untracked files */
  untracked: string[];
  /** Deleted files */
  deleted: string[];
  /** Ahead of remote by N commits */
  ahead: number;
  /** Behind remote by N commits */
  behind: number;
}

export interface GitDiff {
  /** File path */
  path: string;
  /** Type of change */
  type: "added" | "modified" | "deleted" | "renamed";
  /** Old path (for renames) */
  oldPath?: string;
  /** Lines added */
  additions: number;
  /** Lines removed */
  deletions: number;
  /** Diff content (unified format) */
  patch?: string;
}

export interface GitOperationResult {
  /** Operation success */
  success: boolean;
  /** Result message */
  message: string;
  /** Error details if failed */
  error?: string;
  /** Additional data */
  data?: Record<string, unknown>;
}

