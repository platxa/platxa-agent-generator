/**
 * Builds user prompt from error context.
 */
export function buildUserPrompt(context: LLMErrorContext): string {
  const parts: string[] = [];

  parts.push(`ERROR: ${context.errorMessage}`);

  if (context.errorType) {
    parts.push(`TYPE: ${context.errorType}`);
  }

  if (context.filePath) {
    let location = `FILE: ${context.filePath}`;
    if (context.lineNumber) {
      location += `:${context.lineNumber}`;
      if (context.columnNumber) {
        location += `:${context.columnNumber}`;
      }
    }
    parts.push(location);
  }

  if (context.sourceContext) {
    parts.push(`\nSOURCE CONTEXT:\n\`\`\`\n${context.sourceContext}\n\`\`\``);
  }

  if (context.stackTrace) {
    parts.push(`\nSTACK TRACE:\n${context.stackTrace}`);
  }

  parts.push("\nPlease provide a fix for this error.");

  return parts.join("\n");
}

/**
 * Creates an LLM request from error context.
 */
export function createLLMRequest(
  context: LLMErrorContext,
  config: FixGeneratorConfig
): LLMRequest {
  return {
    systemPrompt: config.systemPrompt ?? DEFAULT_SYSTEM_PROMPT,
    userPrompt: buildUserPrompt(context),
    config: config.llmConfig,
  };
}

