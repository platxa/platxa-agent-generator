/**
 * Sends request to LLM provider.
 */
export async function sendToLLM(request: LLMRequest): Promise<LLMResponse> {
  const { config } = request;

  switch (config.provider) {
    case "mock":
      // Parse context from user prompt for mock response
      const errorMatch = request.userPrompt.match(/ERROR:\s*(.+?)(?:\n|$)/);
      const mockContext: LLMErrorContext = {
        errorMessage: errorMatch ? errorMatch[1] : request.userPrompt,
      };
      return getMockResponse(mockContext);

    case "openai":
    case "anthropic":
      // Real providers would be implemented here
      // For now, return error indicating not implemented
      return {
        text: "",
        success: false,
        error: `Provider '${config.provider}' requires API integration. Use 'mock' for testing.`,
      };

    default:
      return {
        text: "",
        success: false,
        error: `Unknown provider: ${config.provider}`,
      };
  }
}

