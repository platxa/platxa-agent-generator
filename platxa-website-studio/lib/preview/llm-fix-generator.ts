/**
 * LLM-based fix generator for novel errors.
 */
export class LLMFixGenerator {
  private config: FixGeneratorConfig;
  private cache: Map<string, LLMGeneratedFix> = new Map();

  constructor(config: Partial<FixGeneratorConfig> = {}) {
    this.config = { ...DEFAULT_FIX_GENERATOR_CONFIG, ...config };
  }

  /**
   * Generates a fix for an error using LLM.
   */
  async generateFix(context: LLMErrorContext): Promise<LLMGeneratedFix> {
    // Check cache
    const cacheKey = this.getCacheKey(context);
    if (this.config.enableCache && this.cache.has(cacheKey)) {
      return this.cache.get(cacheKey)!;
    }

    // Create and send request
    const request = createLLMRequest(context, this.config);
    const response = await sendToLLM(request);

    // Parse response
    const fix = parseLLMResponse(response);

    // Check confidence threshold
    if (fix.confidence < (this.config.minConfidence ?? 0.5)) {
      fix.description = `[Low confidence] ${fix.description}`;
    }

    // Cache result
    if (this.config.enableCache) {
      this.manageCache();
      this.cache.set(cacheKey, fix);
    }

    return fix;
  }

  /**
   * Generates fix synchronously (uses cached mock for testing).
   */
  generateFixSync(context: LLMErrorContext): LLMGeneratedFix {
    const cacheKey = this.getCacheKey(context);
    if (this.cache.has(cacheKey)) {
      return this.cache.get(cacheKey)!;
    }

    // For sync, only mock provider works
    if (this.config.llmConfig.provider !== "mock") {
      return {
        description: "Sync generation only available with mock provider",
        explanation: "Use async generateFix() for real LLM providers",
        confidence: 0,
        isLLMGenerated: false,
      };
    }

    const mockResponse = getMockResponse(context);
    const fix = parseLLMResponse(mockResponse);

    if (this.config.enableCache) {
      this.manageCache();
      this.cache.set(cacheKey, fix);
    }

    return fix;
  }

  /**
   * Checks if error should be sent to LLM (no pattern match).
   */
  shouldUseLLM(hasPatternMatch: boolean): boolean {
    return !hasPatternMatch;
  }

  /**
   * Gets cache key for context.
   */
  private getCacheKey(context: LLMErrorContext): string {
    return JSON.stringify({
      message: context.errorMessage,
      type: context.errorType,
      file: context.filePath,
      line: context.lineNumber,
    });
  }

  /**
   * Manages cache size.
   */
  private manageCache(): void {
    const maxSize = this.config.maxCacheSize ?? 100;
    if (this.cache.size >= maxSize) {
      const firstKey = this.cache.keys().next().value;
      if (firstKey) this.cache.delete(firstKey);
    }
  }

  /**
   * Clears the cache.
   */
  clearCache(): void {
    this.cache.clear();
  }

  /**
   * Gets cache statistics.
   */
  getCacheStats(): { size: number; maxSize: number } {
    return {
      size: this.cache.size,
      maxSize: this.config.maxCacheSize ?? 100,
    };
  }

  /**
   * Updates configuration.
   */
  updateConfig(config: Partial<FixGeneratorConfig>): void {
    this.config = { ...this.config, ...config };
    if (config.llmConfig) {
      this.config.llmConfig = { ...this.config.llmConfig, ...config.llmConfig };
    }
  }

  /**
   * Gets current configuration.
   */
  getConfig(): FixGeneratorConfig {
    return { ...this.config };
  }
}

