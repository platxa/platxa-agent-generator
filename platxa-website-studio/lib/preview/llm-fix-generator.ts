/**
 * Formats a generated fix for display.
 */
export function formatGeneratedFix(fix: LLMGeneratedFix): string {
  const parts: string[] = [];

  parts.push(`Fix: ${fix.description}`);
  parts.push(`Explanation: ${fix.explanation}`);

  if (fix.suggestedCode) {
    parts.push(`\nSuggested Code:\n\`\`\`\n${fix.suggestedCode}\n\`\`\``);
  }

  const confidencePercent = Math.round(fix.confidence * 100);
  parts.push(`\nConfidence: ${confidencePercent}%`);

  if (fix.alternatives && fix.alternatives.length > 0) {
    parts.push("\nAlternatives:");
    for (const alt of fix.alternatives) {
      parts.push(`  - ${alt.description}`);
    }
  }

  if (fix.isLLMGenerated) {
    parts.push("\n[Generated by AI]");
  }

  return parts.join("\n");
}

/**
 * Checks if a fix is high confidence.
 */
export function isHighConfidenceFix(fix: LLMGeneratedFix): boolean {
  return fix.confidence >= 0.8;
}

/**
 * Checks if a fix has code suggestion.
 */
export function hasCodeSuggestion(fix: LLMGeneratedFix): boolean {
  return !!fix.suggestedCode && fix.suggestedCode.trim().length > 0;
}

/**
 * Combines multiple fixes into a summary.
 */
export function combineFixes(fixes: LLMGeneratedFix[]): LLMGeneratedFix {
  if (fixes.length === 0) {
    return {
      description: "No fixes available",
      explanation: "Could not generate any fixes for this error",
      confidence: 0,
      isLLMGenerated: false,
    };
  }

  if (fixes.length === 1) {
    return fixes[0];
  }

  // Take highest confidence fix as primary
  const sorted = [...fixes].sort((a, b) => b.confidence - a.confidence);
  const primary = sorted[0];

  // Others become alternatives
  const alternatives = sorted.slice(1).map((fix) => ({
    description: fix.description,
    suggestedCode: fix.suggestedCode,
  }));

  return {
    ...primary,
    alternatives: [
      ...(primary.alternatives ?? []),
      ...alternatives,
    ],
  };
}
