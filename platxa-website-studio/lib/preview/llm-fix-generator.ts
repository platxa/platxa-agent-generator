/**
 * LLM-Based Fix Generator
 *
 * Generates fixes for novel errors that don't match existing patterns
 * by sending error context to an LLM and parsing the response.
 */

// =============================================================================
// Types
// =============================================================================

/** Error context sent to LLM */
export interface LLMErrorContext {
  /** Error message */
  errorMessage: string;
  /** Error type/category if known */
  errorType?: string;
  /** File path where error occurred */
  filePath?: string;
  /** Line number */
  lineNumber?: number;
  /** Column number */
  columnNumber?: number;
  /** Source code context (lines around error) */
  sourceContext?: string;
  /** Stack trace if available */
  stackTrace?: string;
  /** Additional metadata */
  metadata?: Record<string, unknown>;
}

/** Generated fix from LLM */
export interface LLMGeneratedFix {
  /** Fix description */
  description: string;
  /** Suggested code change */
  suggestedCode?: string;
  /** Explanation of the fix */
  explanation: string;
  /** Confidence level (0-1) */
  confidence: number;
  /** Alternative fixes if any */
  alternatives?: Array<{
    description: string;
    suggestedCode?: string;
  }>;
  /** Whether this was generated by LLM */
  isLLMGenerated: boolean;
  /** Raw LLM response for debugging */
  rawResponse?: string;
}

/** LLM provider configuration */
export interface LLMProviderConfig {
  /** Provider name */
  provider: "openai" | "anthropic" | "mock";
  /** API key (for real providers) */
  apiKey?: string;
  /** Model to use */
  model?: string;
  /** Maximum tokens in response */
  maxTokens?: number;
  /** Temperature for generation */
  temperature?: number;
  /** Request timeout in ms */
  timeout?: number;
}

/** LLM request */
export interface LLMRequest {
  /** System prompt */
  systemPrompt: string;
  /** User prompt with error context */
  userPrompt: string;
  /** Provider config */
  config: LLMProviderConfig;
}

/** LLM response */
export interface LLMResponse {
  /** Generated text */
  text: string;
  /** Whether request was successful */
  success: boolean;
  /** Error message if failed */
  error?: string;
  /** Token usage */
  usage?: {
    promptTokens: number;
    completionTokens: number;
    totalTokens: number;
  };
}

/** Fix generator configuration */
export interface FixGeneratorConfig {
  /** LLM provider config */
  llmConfig: LLMProviderConfig;
  /** System prompt template */
  systemPrompt?: string;
  /** Enable caching of responses */
  enableCache?: boolean;
  /** Maximum cache size */
  maxCacheSize?: number;
  /** Minimum confidence threshold */
  minConfidence?: number;
}

