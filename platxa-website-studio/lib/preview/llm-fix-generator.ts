// Response Parsing
// =============================================================================

/**
 * Parses confidence level from text.
 */
export function parseConfidence(text: string): number {
  const lowered = text.toLowerCase();

  if (lowered.includes("high")) return 0.9;
  if (lowered.includes("medium")) return 0.7;
  if (lowered.includes("low")) return 0.4;

  // Try to parse numeric confidence
  const numMatch = lowered.match(/(\d+(?:\.\d+)?)\s*%?/);
  if (numMatch) {
    const num = parseFloat(numMatch[1]);
    return num > 1 ? num / 100 : num;
  }

  return 0.6; // Default medium confidence
}

/**
 * Extracts code block from LLM response.
 */
export function extractCodeBlock(text: string): string | undefined {
  // Match fenced code blocks
  const fencedMatch = text.match(/```(?:\w+)?\s*\n([\s\S]*?)\n```/);
  if (fencedMatch) {
    return fencedMatch[1].trim();
  }

  // Match indented code (4+ spaces)
  const indentedLines: string[] = [];
  let inCodeBlock = false;

  for (const line of text.split("\n")) {
    if (line.match(/^(?:\s{4,}|\t)/)) {
      inCodeBlock = true;
      indentedLines.push(line.replace(/^(?:\s{4}|\t)/, ""));
    } else if (inCodeBlock && line.trim() === "") {
      indentedLines.push("");
    } else if (inCodeBlock) {
      break;
    }
  }

  if (indentedLines.length > 0) {
    return indentedLines.join("\n").trim();
  }

  return undefined;
}

/**
 * Extracts section from LLM response.
 */
export function extractSection(text: string, sectionName: string): string {
  const patterns = [
    new RegExp(`${sectionName}:\\s*(.+?)(?=\\n[A-Z]+:|$)`, "is"),
    new RegExp(`\\*\\*${sectionName}\\*\\*:\\s*(.+?)(?=\\n\\*\\*|$)`, "is"),
    new RegExp(`#+ ${sectionName}\\s*\\n(.+?)(?=\\n#|$)`, "is"),
  ];

  for (const pattern of patterns) {
    const match = text.match(pattern);
    if (match) {
      return match[1].trim();
    }
  }

  return "";
}

/**
 * Parses LLM response into a generated fix.
 */
export function parseLLMResponse(response: LLMResponse): LLMGeneratedFix {
  if (!response.success || !response.text) {
    return {
      description: "Failed to generate fix",
      explanation: response.error ?? "Unknown error occurred",
      confidence: 0,
      isLLMGenerated: true,
      rawResponse: response.text,
    };
  }

  const text = response.text;

  // Extract sections
  const description = extractSection(text, "DESCRIPTION") ||
    extractSection(text, "Fix") ||
    text.split("\n")[0].replace(/^[#*\-\d.]+\s*/, "").trim();

  const explanation = extractSection(text, "EXPLANATION") ||
    extractSection(text, "Why") ||
    extractSection(text, "Cause") ||
    "See the suggested fix below.";

  const confidenceText = extractSection(text, "CONFIDENCE") || "medium";
  const confidence = parseConfidence(confidenceText);

  const suggestedCode = extractCodeBlock(text) ||
    extractSection(text, "CODE") ||
    undefined;

  // Extract alternatives if present
  const alternatives: Array<{ description: string; suggestedCode?: string }> = [];
  const altMatch = text.match(/(?:alternative|other|also)[\s\S]*?(?:```[\s\S]*?```|$)/gi);
  if (altMatch) {
    for (const alt of altMatch) {
      const altCode = extractCodeBlock(alt);
      const altDesc = alt.split("\n")[0].replace(/^[#*\-\d.]+\s*/, "").trim();
      if (altDesc && altDesc !== description) {
        alternatives.push({ description: altDesc, suggestedCode: altCode });
      }
    }
  }

  return {
    description: description || "Fix suggested by AI",
    explanation,
    suggestedCode,
    confidence,
    alternatives: alternatives.length > 0 ? alternatives : undefined,
    isLLMGenerated: true,
    rawResponse: text,
  };
}

