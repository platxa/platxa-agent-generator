/**
 * Theme Export System
 *
 * Production-grade export for Odoo themes using JSZip.
 * Handles ZIP creation, validation, and multiple output formats.
 */

import JSZip from "jszip";
import { validateTheme, formatValidationResult } from "../odoo-skills";
import type { GeneratedFile, ValidationResult } from "../odoo-skills";

// =============================================================================
// TYPES
// =============================================================================

export interface ExportOptions {
  /** Theme module name */
  themeName: string;
  /** Files to include in export */
  files: GeneratedFile[];
  /** Include validation in export */
  validate?: boolean;
  /** Include README */
  includeReadme?: boolean;
  /** Include .gitignore */
  includeGitignore?: boolean;
  /** Author name for manifest */
  author?: string;
  /** Website URL for manifest */
  website?: string;
}

export interface ExportResult {
  /** Success status */
  success: boolean;
  /** ZIP blob for download */
  blob?: Blob;
  /** Validation result if validation was requested */
  validation?: ValidationResult;
  /** Error message if failed */
  error?: string;
  /** Export statistics */
  stats: {
    fileCount: number;
    totalSize: number;
    validationErrors: number;
    validationWarnings: number;
  };
}

