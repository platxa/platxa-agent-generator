/**
 * Run interactive prompts for generation options
 */
async function runInteractivePrompts(
  initialName?: string
): Promise<GenerateOptions> {
  const rl = createPrompt()

  console.log("\n🎨 Platxa Brand Kit Generator\n")
  console.log("This will create a new brand kit package.\n")

  try {
    // Package name
    const defaultName = initialName || "my-brand-kit"
    const name = (await ask(rl, `Package name [${defaultName}]: `)) || defaultName

    // Output directory
    const defaultDir = name.replace(/^@[^/]+\//, "")
    const outputDir =
      (await ask(rl, `Output directory [./${defaultDir}]: `)) ||
      `./${defaultDir}`

    // Description
    const description = await ask(rl, "Description (optional): ")

    // Author
    const author = await ask(rl, "Author (optional): ")

    // Preset
    const presetOptions = ["default", "blue", "green", "violet"] as const
    console.log("\nPreset colors:")
    for (const p of presetOptions) {
      const config = PRESET_CONFIGS[p]
      console.log(`  - ${p}: ${config.name}`)
    }
    const preset = await askChoice(rl, "\nSelect preset:", presetOptions, 0)

    // Primary hue override
    const hueStr = await ask(
      rl,
      `Primary hue (0-360) [${PRESET_CONFIGS[preset].primaryHue}]: `
    )
    const primaryHue = hueStr
      ? parseInt(hueStr, 10)
      : PRESET_CONFIGS[preset].primaryHue

    // TypeScript
    const typescript = await askYesNo(rl, "\nUse TypeScript?", true)

    // Include CSS
    const includeCss = await askYesNo(rl, "Include CSS file?", false)

    // Include Tailwind preset
    const includeTailwind = await askYesNo(rl, "Include Tailwind preset?", true)

    rl.close()

    return {
      name,
      outputDir,
      preset,
      primaryHue,
      typescript,
      includeCss,
      includeTailwind,
      author: author || undefined,
      description: description || undefined,
    }
  } catch (error) {
    rl.close()
    throw error
  }
}

