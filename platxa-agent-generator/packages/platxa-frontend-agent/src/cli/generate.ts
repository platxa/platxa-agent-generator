/**
 * CLI entry point
 */
export async function main(): Promise<void> {
  const args = process.argv.slice(2)

  // Handle help
  if (args.includes("--help") || args.includes("-h")) {
    console.log(`
Usage: platxa-generate [name] [options]

Generate a brand kit package scaffold.

Arguments:
  name                 Package name (e.g., @acme/brand-kit)

Options:
  --output, -o <dir>   Output directory (default: derived from name)
  --preset <name>      Color preset (default, blue, green, violet)
  --hue <number>       Primary hue (0-360)
  --typescript, --ts   Generate TypeScript (default: true)
  --javascript, --js   Generate JavaScript
  --author <name>      Package author
  --description <text> Package description
  -h, --help           Show this help message

Examples:
  platxa-generate my-brand-kit
  platxa-generate @acme/brand-kit --preset blue
  platxa-generate my-brand --hue 180 --js
`)
    process.exit(0)
  }

  // Parse CLI args
  let name: string | undefined
  let outputDir: string | undefined
  let preset: GenerateOptions["preset"] = "default"
  let primaryHue: number | undefined
  let typescript = true
  let author: string | undefined
  let description: string | undefined

  for (let i = 0; i < args.length; i++) {
    const arg = args[i]

    if (arg === "--output" || arg === "-o") {
      outputDir = args[++i]
    } else if (arg === "--preset") {
      const p = args[++i]
      if (["default", "blue", "green", "violet"].includes(p)) {
        preset = p as GenerateOptions["preset"]
      }
    } else if (arg === "--hue") {
      primaryHue = parseInt(args[++i], 10)
    } else if (arg === "--typescript" || arg === "--ts") {
      typescript = true
    } else if (arg === "--javascript" || arg === "--js") {
      typescript = false
    } else if (arg === "--author") {
      author = args[++i]
    } else if (arg === "--description") {
      description = args[++i]
    } else if (!arg.startsWith("-") && !name) {
      name = arg
    }
  }

  // If no name provided, run interactive mode
  if (!name) {
    const options = await runInteractivePrompts()
    const result = await generateBrandKit(options)
    process.exit(result.success ? 0 : 1)
  }

  // Non-interactive mode
  const options: GenerateOptions = {
    name,
    outputDir: outputDir || `./${name.replace(/^@[^/]+\//, "")}`,
    preset,
    primaryHue: primaryHue || PRESET_CONFIGS[preset].primaryHue,
    typescript,
    includeCss: false,
    includeTailwind: true,
    author,
    description,
  }

  const result = await generateBrandKit(options)

  if (!result.success) {
    console.error(`\n❌ Error: ${result.error}\n`)
  }

  process.exit(result.success ? 0 : 1)
}

// Run if executed directly
const isMain =
  typeof require !== "undefined"
    ? require.main === module
    : import.meta.url === `file://${process.argv[1]}`

if (isMain) {
  main().catch((err) => {
    console.error("Error:", err.message)
    process.exit(1)
  })
}
