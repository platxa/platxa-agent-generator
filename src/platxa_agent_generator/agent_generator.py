def _select_output_format(definition: AgentDefinition) -> str:
    """Select the best output format template based on agent characteristics.

    Selection logic:
    - Orchestrators with workers -> orchestrator_json
    - Agents with security/review/scan in name -> json_report
    - Agents with analyze/check/audit -> markdown_table
    - Agents with generate/build/create -> json_data
    - Agents with report/document -> structured_report
    - Default -> json_data
    """
    name_lower = definition.name.lower()
    desc_lower = definition.description.lower() if definition.description else ""
    combined = f"{name_lower} {desc_lower}"

    if definition.workers:
        return "orchestrator_json"

    if any(kw in combined for kw in ["security", "scan", "lint", "review", "vulnerab"]):
        return "json_report"

    if any(kw in combined for kw in ["analyze", "check", "audit", "test", "validate"]):
        return "markdown_table"

    if any(kw in combined for kw in ["report", "document", "summarize", "diagnos"]):
        return "structured_report"

    return "json_data"


def generate_output_section(definition: AgentDefinition) -> str:
    """Generate Output Format section with concrete, format-specific templates.

    Selects the appropriate output format (JSON report, Markdown table,
    structured report, or data JSON) based on agent name, description,
    and architecture (orchestrator vs standalone).
    """
    lines = ["## Output Format", ""]

    format_key = _select_output_format(definition)
    template = OUTPUT_FORMAT_TEMPLATES[format_key]

    # Substitute agent name into template
    template = template.replace("<agent-name>", definition.name)

    # For orchestrators, substitute worker names
    if definition.workers and format_key == "orchestrator_json":
        for worker in definition.workers:
            template = template.replace("<worker-name>", worker.name, 1)

    lines.append(template)
    lines.append("")
    return "\n".join(lines)


def generate_workers_section(definition: AgentDefinition) -> str:
    """Generate Worker Definitions section for orchestrators."""
    if not definition.workers:
        return ""

    lines = ["## Worker Definitions", ""]

    for worker in definition.workers:
        lines.append(f"### {worker.name}")
        lines.append("")
        lines.append(f"**Role:** {worker.role}")
        lines.append("")
        lines.append(f"**Tools:** {', '.join(worker.tools)}")
        lines.append("")

        if worker.input_format:
            lines.append("**Input:**")
            lines.append("```")
            lines.append(worker.input_format)
            lines.append("```")
            lines.append("")

        if worker.output_format:
            lines.append("**Output:**")
            lines.append("```")
            lines.append(worker.output_format)
            lines.append("```")
            lines.append("")

    return "\n".join(lines)


def generate_attribution_footer(
    definition: AgentDefinition,
    pattern: str = "prompt-chaining",
    quality_score: float | None = None,
    timestamp: str | None = None,
) -> str:
    """Render the HTML-comment attribution footer (feature #79).

    Every generated agent gets a trailing block of HTML comments so
    operators can audit who/what/when produced the file without the
    metadata polluting the rendered Markdown view (HTML comments are
    invisible in standard Markdown renderers). The block records:

    - Generator version (``PLATXA_GENERATOR_VERSION``) — for matching
      the file against generator changelog entries when investigating
      a regression.
    - UTC timestamp — when the file was emitted, useful for stale-file
      detection and "when did this break" forensics.
    - Quality score — the validation score the file passed at, when
      the caller has it. Surfaced inline so reviewers can see the gate
      bar without re-running the validator.
    - Generation parameters — the workflow pattern, agent name,
      declared tools, and model choice. These are the inputs that
      determined the file's shape; recording them makes it easy to
      reproduce the generation deterministically.

    Args:
        definition: The agent the file was generated from.
        pattern: The composition pattern used (e.g. ``"prompt-chaining"``,
            ``"orchestrator-workers"``).
        quality_score: Optional 0-10 quality score from the validator.
            ``None`` means "not measured" and is reported as such — the
            footer should not lie about a missing score by emitting 0.
        timestamp: Optional ISO-8601 UTC timestamp override. Used by
            tests to pin a deterministic value; production callers leave
            this ``None`` and get ``datetime.now(timezone.utc)``.

    Returns:
        Multi-line HTML comment block. Starts with
        :data:`GENERATION_FOOTER_MARKER` so downstream tooling can
        locate it.
    """
    if timestamp is None:
        # Use ISO-8601 with the trailing 'Z' so the UTC convention is
        # explicit at a glance — datetime's ``+00:00`` form is correct
        # but harder to scan when the user is reading the comment.
        timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

    score_line = (
        f"     Quality Score: {quality_score:.2f}/10"
        if quality_score is not None
        else "     Quality Score: not measured"
    )
    tools_str = ", ".join(definition.tools) if definition.tools else "(none)"
    model_str = definition.model if definition.model else "(default)"

    lines = [
        f"<!-- Generated by platxa-agent-generator v{PLATXA_GENERATOR_VERSION}",
        f"     Timestamp:     {timestamp}",
        score_line,
        "     Parameters:",
        f"       name:    {definition.name}",
        f"       pattern: {pattern}",
        f"       model:   {model_str}",
        f"       tools:   {tools_str}",
        "-->",
    ]
    return "\n".join(lines)


def generate_agent_file(
    definition: AgentDefinition,
    pattern: str = "prompt-chaining",
    quality_score: float | None = None,
    timestamp: str | None = None,
) -> str:
    """Generate complete agent file content with 14 sections.

    Sections (in order):
    1. Overview - Brief description and purpose
    2. Prerequisites - Dependencies, permissions, environment
    3. Workflow - Step-by-step execution flow
    4. Workers (if orchestrator) - Worker definitions
    5. Examples (x3) - Basic, advanced, edge case
    6. Error Handling - Failure modes, recovery, retries
    7. Edge Cases - Boundary conditions, special scenarios
    8. Security Considerations - Threat model, input validation
    9. Boundaries - What agent MUST NOT do
    10. Notes & Limitations - Caveats, known issues
    11. Tool Reference - When to use/not use each tool
    12. Output Format - Expected response structure
    13. Verification - Success criteria, verification commands, expected output
    14. Related Agents - Composition patterns
    """
    parts = []

    # Frontmatter
    parts.append(generate_frontmatter(definition))
    parts.append("")

    # Title
    parts.append(generate_title(definition.name))
    parts.append("")

    # 1. Overview
    parts.append(generate_overview_section(definition))

    # 2. Prerequisites
    parts.append(generate_prerequisites_section(definition))

    # Custom sections (if any)
    for section in definition.sections:
        heading = "#" * section.level + " " + section.title
        parts.append(heading)
        parts.append("")
        parts.append(section.content)
        parts.append("")

    # 3. Workflow
    parts.append(generate_workflow_section(definition, pattern))

    # 4. Workers (for orchestrator pattern)
    workers_section = generate_workers_section(definition)
    if workers_section:
        parts.append(workers_section)

    # 5. Examples (3 examples: basic, advanced, edge case)
    parts.append(generate_examples_section(definition))

    # 6. Error Handling
    parts.append(generate_error_handling_section(definition))

    # 7. Edge Cases
    parts.append(generate_edge_cases_section(definition))

    # 8. Security Considerations
    parts.append(generate_security_section(definition))

    # 9. Boundaries
    parts.append(generate_boundaries_section(definition))

    # 10. Notes & Limitations
    parts.append(generate_notes_section(definition))

    # 11. Tool Reference (when to use, when not to use)
    tool_ref = generate_tool_reference_section(definition)
    if tool_ref:
        parts.append(tool_ref)

    # 12. Output Format
    parts.append(generate_output_section(definition))

    # 13. Verification
    parts.append(generate_verification_section(definition))

    # 14. Related Agents
    parts.append(generate_related_agents_section(definition))

    # 15. Attribution footer (HTML comment, invisible in rendered
    # Markdown). Records generator version, timestamp, quality score,
    # and generation parameters so operators can audit who/what/when
    # produced the file. Always last so its position is stable for
    # downstream tooling that locates it via GENERATION_FOOTER_MARKER.
    parts.append("")
    parts.append(generate_attribution_footer(definition, pattern, quality_score, timestamp))

    return "\n".join(parts)


# ─── Context Budget Estimation ─────────────────────────────────────────────────
# Agent definitions are injected into the system prompt, consuming context window.
# Large definitions reduce available context for the conversation.
# Thresholds based on Claude Code's typical context budget allocation:
#   - <2000 tokens: OK (leaves >95% of context for conversation)
#   - 2000-5000 tokens: WARNING (may crowd out conversation context)
#   - >5000 tokens: ERROR (likely too large, will degrade agent quality)

CONTEXT_BUDGET_WARN_TOKENS = 2000
CONTEXT_BUDGET_ERROR_TOKENS = 5000
CHARS_PER_TOKEN = 4  # Conservative heuristic (actual is ~3.5 for English)


@dataclass
class ContextBudgetResult:
    """Result of context budget estimation."""

    char_count: int
    estimated_tokens: int
    status: str  # "ok", "warning", "error"
    message: str
    suggestions: list[str]


def estimate_context_budget(content: str) -> ContextBudgetResult:
    """Estimate token cost of agent definition and check budget thresholds.

    Uses a 4-chars-per-token heuristic (conservative for English text with
    markdown formatting and YAML frontmatter).

    Args:
        content: Full agent definition file content

    Returns:
        ContextBudgetResult with token estimate, status, and suggestions.
    """
    char_count = len(content)
    estimated_tokens = char_count // CHARS_PER_TOKEN

    suggestions: list[str] = []

    if estimated_tokens > CONTEXT_BUDGET_ERROR_TOKENS:
        status = "error"
        message = (
            f"Agent definition is ~{estimated_tokens} tokens ({char_count} chars) "
            f"— exceeds {CONTEXT_BUDGET_ERROR_TOKENS} token limit. "
            "This will significantly reduce available context for conversation."
        )
        suggestions.extend(
            [
                "Remove verbose examples — keep 2-3 concise ones",
                "Shorten workflow descriptions — use bullet points not paragraphs",
                "Move detailed documentation to a companion file",
                "Remove redundant sections (e.g. Notes if covered in Workflow)",
                "Reduce tool reference section — only document non-obvious usage",
            ]
        )
    elif estimated_tokens > CONTEXT_BUDGET_WARN_TOKENS:
        status = "warning"
        message = (
            f"Agent definition is ~{estimated_tokens} tokens ({char_count} chars) "
            f"— approaching {CONTEXT_BUDGET_ERROR_TOKENS} token limit. "
            "Consider pruning to leave more context for conversation."
        )
        suggestions.extend(
            [
                "Trim examples to essential patterns only",
                "Condense workflow steps — remove obvious substeps",
                "Consider splitting into a main agent + helper agents",
            ]
        )
    else:
        status = "ok"
        message = (
            f"Agent definition is ~{estimated_tokens} tokens ({char_count} chars) — within budget."
        )

    return ContextBudgetResult(
        char_count=char_count,
        estimated_tokens=estimated_tokens,
        status=status,
        message=message,
        suggestions=suggestions,
    )


def create_definition_from_dict(data: dict[str, Any]) -> AgentDefinition:
    """Create AgentDefinition from dictionary."""
    # Parse tools via the canonical normalizer (feature #27). The
    # previous inline ``[t.strip() for t in tools.split(",")]`` kept
    # empty strings produced by trailing/doubled commas — those are
    # always authoring errors that the normalizer now drops uniformly
    # across the suite. ``parse_tools_string`` also accepts a list
    # input so the pre-existing ``isinstance(tools, str)`` branch
    # collapses into a single call.
    tools = parse_tools_string(data.get("tools", []))

    # Parse workers
    workers = []
    for w in data.get("workers", []):
        worker_tools = parse_tools_string(w.get("tools", []))
        workers.append(
            WorkerDefinition(
                name=w.get("name", "worker"),
                role=w.get("role", ""),
                tools=worker_tools,
                input_format=w.get("input_format", ""),
                output_format=w.get("output_format", ""),
            )
        )

    # Parse sections
    sections = []
    for s in data.get("sections", []):
        sections.append(
            AgentSection(
                title=s.get("title", ""),
                content=s.get("content", ""),
                level=s.get("level", 2),
            )
        )

    # Parse chain_steps for prompt-chaining pattern
    chain_steps = []
    for cs in data.get("chain_steps", []):
        step_tools = parse_tools_string(cs.get("tools", []))
        chain_steps.append(
            ChainStep(
                name=cs.get("name", "Step"),
                description=cs.get("description", ""),
                input_from=cs.get("input_from", ""),
                output_to=cs.get("output_to", ""),
                tools=step_tools,
                validation=cs.get("validation", ""),
            )
        )

    # Parse template variables
    template_vars = TemplateVariables()
    if "template_vars" in data:
        template_vars = TemplateVariables.from_dict(data["template_vars"])

    return AgentDefinition(
        name=data.get("name", "unnamed-agent"),
        description=data.get("description", ""),
        tools=tools,
        sections=sections,
        workers=workers,
        chain_steps=chain_steps,
        examples=data.get("examples", []),
        metadata=data.get("metadata", {}),
        template_vars=template_vars,
    )


def generate(
    name: str,
    description: str,
    tools: list[str],
    pattern: str = "prompt-chaining",
    output_path: str | None = None,
    context_hint: str = "",
    discovery_context: dict[str, Any] | None = None,
    **kwargs: Any,
) -> tuple[bool, str, str]:
    """Generate an agent file.

    Args:
        context_hint: Optional targeted-reprompt context from a prior iteration.
            Prepended to the description to steer regeneration toward fixing
            specific quality deficiencies.
        discovery_context: Output from ``context_discovery.discovery_report()``.
            Piped into the agent definition so downstream sections (e.g.
            Related Agents) can reference actually-existing agents.

    Returns:
        (success, content_or_error, output_path)
    """
    # Validate inputs
    valid, error = validate_name(name)
    if not valid:
        return False, f"Invalid name: {error}", ""

    valid, error = validate_description(description)
    if not valid:
        return False, f"Invalid description: {error}", ""

    valid, error, normalized_tools = validate_tools(tools)
    if not valid:
        return False, f"Invalid tools: {error}", ""

    # Enrich description with reprompt context when retrying
    effective_description = description
    if context_hint:
        effective_description = f"{description}\n\n[Regeneration guidance]\n{context_hint}"

    # Create definition
    template_vars = kwargs.get("template_vars", TemplateVariables())
    if isinstance(template_vars, dict):
        template_vars = TemplateVariables.from_dict(template_vars)

    definition = AgentDefinition(
        name=name,
        description=effective_description,
        tools=normalized_tools,
        sections=kwargs.get("sections", []),
        workers=kwargs.get("workers", []),
        chain_steps=kwargs.get("chain_steps", []),
        examples=kwargs.get("examples", []),
        metadata=kwargs.get("metadata", {}),
        discovery_context=discovery_context or {},
        template_vars=template_vars,
    )

    # Generate content
    content = generate_agent_file(definition, pattern)

    # Write to file if output path specified
    if output_path:
        # Validate output path
        valid, error = validate_path_safe(output_path, [".md", ""])
        if not valid:
            return False, f"Invalid output path: {error}", ""

        path = Path(output_path)
        if path.is_dir():
            path = path / f"{name}.md"
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)
        return True, content, str(path)

    return True, content, ""


def load_blueprint_file(filepath: str) -> dict[str, Any]:
    """Safely load a blueprint JSON file."""
    # Validate path
    valid, error = validate_path_safe(filepath, [".json"])
    if not valid:
        raise ValueError(f"Invalid blueprint path: {error}")

    path = Path(filepath).resolve()

    # Additional check: ensure file exists
    if not path.exists():
        raise FileNotFoundError(f"Blueprint file not found: {filepath}")

    if not path.is_file():
        raise ValueError(f"Blueprint path is not a file: {filepath}")

    # Read and parse JSON
    content = path.read_text(encoding="utf-8")
    return json.loads(content)


