def validate_path_safe(filepath: str, allowed_extensions: list[str]) -> tuple[bool, str]:
    """Validate file path to prevent path traversal attacks."""
    import tempfile

    path = Path(filepath)

    # Check for path traversal attempts
    try:
        resolved = path.resolve()
        # Ensure path is within allowed directories:
        # - Current working directory
        # - User home directory
        # - System temp directory (for testing)
        cwd = Path.cwd().resolve()
        home = Path.home().resolve()
        temp = Path(tempfile.gettempdir()).resolve()

        allowed_roots = [cwd, home, temp]
        if not any(resolved.is_relative_to(root) for root in allowed_roots):
            return (
                False,
                "Path must be within current directory, home directory, or temp directory",
            )
    except (OSError, ValueError) as e:
        return False, f"Invalid path: {e}"

    # Check extension
    if allowed_extensions and path.suffix.lower() not in allowed_extensions:
        return False, f"Invalid extension. Allowed: {', '.join(allowed_extensions)}"

    # Check for suspicious shell metacharacters in the raw input
    suspicious_patterns = ["~", "$", "`", ";", "|", "&"]
    filepath_str = str(filepath)
    for pattern in suspicious_patterns:
        if pattern in filepath_str:
            return False, f"Suspicious pattern in path: {pattern}"

    return True, ""


def validate_name(name: str) -> tuple[bool, str]:
    """Validate agent name format."""
    if not name:
        return False, "Name cannot be empty"

    if len(name) > 64:
        return False, f"Name too long ({len(name)} > 64 chars)"

    if not re.match(r"^[a-z][a-z0-9-]*[a-z0-9]$", name) and len(name) > 1:
        if not re.match(r"^[a-z][a-z0-9-]*$", name):
            return (
                False,
                "Name must be hyphen-case (lowercase letters, numbers, hyphens)",
            )

    if "--" in name:
        return False, "Name cannot contain consecutive hyphens"

    return True, ""


def validate_description(description: str) -> tuple[bool, str]:
    """Validate description format."""
    if not description:
        return False, "Description cannot be empty"

    if len(description) > 1024:
        return False, f"Description too long ({len(description)} > 1024 chars)"

    return True, ""


def validate_tools(tools: list[str]) -> tuple[bool, str, list[str]]:
    """Validate and normalize tool names."""
    if not tools:
        return False, "At least one tool required", []

    normalized = []
    invalid = []

    for tool in tools:
        tool_clean = tool.strip()
        # Check for MCP tools (mcp__*)
        if tool_clean.startswith("mcp__"):
            normalized.append(tool_clean)
        elif tool_clean in VALID_TOOLS:
            normalized.append(tool_clean)
        else:
            # Try case-insensitive match
            matched = False
            for valid in VALID_TOOLS:
                if tool_clean.lower() == valid.lower():
                    normalized.append(valid)
                    matched = True
                    break
            if not matched:
                invalid.append(tool_clean)

    if invalid:
        return False, f"Invalid tools: {', '.join(invalid)}", normalized

    return True, "", normalized


def build_validation_failure_context(
    report: QualityReport,
    *,
    min_score: float = 7.0,
    rubric: EvaluationRubric | None = None,
) -> str:
    """Convert a failed QualityReport into a targeted regeneration prompt.

    Bridges the per-criterion findings from ``quality_scorer`` into the
    format expected by ``targeted_reprompt.build_regeneration_prompt()``,
    using ``verdict_aggregator`` to classify axes as blocking or warning.

    Returns an empty string when the report passes (no regeneration needed).
    """
    if report.passed:
        return ""

    from platxa_agent_generator.evaluation_criteria import EvaluationRubric as _Rubric
    from platxa_agent_generator.targeted_reprompt import build_regeneration_prompt
    from platxa_agent_generator.verdict_aggregator import aggregate_from_rubric

    if rubric is None:
        rubric = _Rubric.load_default()

    unmet_axes: list[str] = []
    findings_dicts: list[dict[str, str]] = []
    known_axes = _axis_names_from_rubric(rubric)

    for criterion in report.criteria:
        axis_name = criterion.name
        if axis_name not in known_axes:
            continue
        if criterion.score < min_score:
            unmet_axes.append(axis_name)
            severity = rubric.axis(axis_name).severity_on_unmet
            for finding_text in criterion.findings:
                findings_dicts.append(
                    {
                        "axis": axis_name,
                        "severity": severity,
                        "summary": finding_text,
                        "location": "",
                    }
                )
            for suggestion_text in criterion.suggestions:
                findings_dicts.append(
                    {
                        "axis": axis_name,
                        "severity": severity,
                        "summary": f"[suggestion] {suggestion_text}",
                        "location": "",
                    }
                )

    if not unmet_axes:
        return ""

    verdict = aggregate_from_rubric(unmet_axes, rubric)

    return build_regeneration_prompt(
        findings=findings_dicts,
        blocking_axes=list(verdict.blocking_axes),
        warning_axes=list(verdict.warning_axes),
        rubric=rubric,
    )


def _axis_names_from_rubric(rubric: EvaluationRubric) -> frozenset[str]:
    """Extract the set of axis names from a rubric instance."""
    return frozenset(a.name for a in rubric.axes)


def generate_frontmatter(definition: AgentDefinition) -> str:
    """Generate valid YAML frontmatter.

    Emits all Claude Code agent frontmatter fields that are set on the
    definition, in the canonical order: name, description, tools,
    permissionMode, then optional metadata fields.
    """
    lines = ["---"]
    lines.append(f"name: {definition.name}")

    # Collapse whitespace to prevent frontmatter injection via embedded newlines
    desc = re.sub(r"\s+", " ", definition.description).strip()
    if ":" in desc or desc.startswith("{") or desc.startswith("["):
        # Use quoted string for complex descriptions
        desc_escaped = desc.replace('"', '\\"')
        lines.append(f'description: "{desc_escaped}"')
    else:
        lines.append(f"description: {desc}")

    # Tools as comma-separated list
    lines.append(f"tools: {', '.join(definition.tools)}")

    # Permission mode — only emit when explicitly set
    if definition.permission_mode and definition.permission_mode in VALID_PERMISSION_MODES:
        lines.append(f"permissionMode: {definition.permission_mode}")

    # Max turns — only emit when explicitly set and valid
    if definition.max_turns is not None and definition.max_turns > 0:
        lines.append(f"maxTurns: {definition.max_turns}")

    # Model tier — only emit when explicitly set
    if definition.model and definition.model in VALID_MODELS:
        lines.append(f"model: {definition.model}")

    # Disallowed tools — defense-in-depth complement to tools list
    if definition.disallowed_tools:
        lines.append(f"disallowedTools: {', '.join(definition.disallowed_tools)}")

    # Isolation mode — only emit when explicitly set
    if definition.isolation and definition.isolation in VALID_ISOLATION_MODES:
        lines.append(f"isolation: {definition.isolation}")

    # Effort level — controls extended thinking intensity
    if definition.effort and definition.effort in VALID_EFFORT_LEVELS:
        lines.append(f"effort: {definition.effort}")

    # Background mode — run without blocking main conversation
    if definition.background is True:
        lines.append("background: true")

    # Color — visual categorization in Claude Code UI
    if definition.color:
        lines.append(f"color: {definition.color}")

    # Skills — list of named Claude Code skills the agent should know about.
    # Emitted as a comma-separated list so the format mirrors `tools:` and
    # round-trips cleanly through YAML parsers. Skipped when empty so simple
    # agents stay terse.
    if definition.skills:
        lines.append(f"skills: {', '.join(definition.skills)}")

    # MCP servers — nested YAML block for external tool integrations
    # Format: mcpServers:\n  server-name:\n    command: ...\n    args: [...]
    if definition.mcp_servers:
        import yaml

        mcp_yaml = yaml.dump(
            {"mcpServers": definition.mcp_servers},
            default_flow_style=False,
            sort_keys=False,
        ).strip()
        lines.append(mcp_yaml)

    # Add metadata if present
    if definition.metadata:
        if "version" in definition.metadata:
            lines.append(f"version: {definition.metadata['version']}")
        if "author" in definition.metadata:
            lines.append(f"author: {definition.metadata['author']}")

    lines.append("---")
    return "\n".join(lines)


# Default location for project-scoped Claude Code skills. Centralized as a
# constant so the skill-discovery helpers don't repeat the literal and so
# tests can override it cleanly. Other code that needs to look up skills
# (e.g. the agent-generator's recommendation pass) imports this constant.
DEFAULT_SKILLS_DIR: str = ".claude/skills"

# Filename inside each skill directory that carries the skill's frontmatter
# (name + description + allowed-tools). Pinned to the Claude Code convention.
SKILL_MANIFEST_FILENAME: str = "SKILL.md"

# Cap on how many skills the recommender returns by default. Keeps frontmatter
# terse and forces the recommender to be selective rather than dumping the
# whole catalog.
DEFAULT_SKILL_RECOMMENDATION_LIMIT: int = 5


def _parse_skill_manifest(manifest_path: Path) -> tuple[str, str] | None:
    """Read a SKILL.md and extract (name, description) from its YAML frontmatter.

    Returns ``None`` when the file is missing, unreadable, missing
    frontmatter, or missing the required fields. Never raises — discovery
    must keep going across the rest of the catalog if one skill is
    malformed (a single broken SKILL.md should not blind the agent to all
    other skills).
    """
    try:
        text = manifest_path.read_text(encoding="utf-8")
    except OSError:
        return None
    if not text.startswith("---"):
        return None
    try:
        end = text.index("\n---", 3)
    except ValueError:
        return None
    frontmatter = text[3:end]
    name: str | None = None
    description: str | None = None
    for line in frontmatter.splitlines():
        stripped = line.strip()
        if stripped.startswith("name:"):
            name = stripped.split(":", 1)[1].strip()
        elif stripped.startswith("description:"):
            description = stripped.split(":", 1)[1].strip()
        if name and description:
            break
    if not name or not description:
        return None
    return name, description


def discover_available_skills(skills_dir: Path | str = DEFAULT_SKILLS_DIR) -> dict[str, str]:
    """Scan ``skills_dir`` for SKILL.md manifests and return ``{name: description}``.

    Each immediate subdirectory of ``skills_dir`` is treated as a skill
    candidate. The skill is included only when its ``SKILL.md`` parses
    successfully and exposes both ``name`` and ``description`` in its
    YAML frontmatter.

    Returns an empty dict when ``skills_dir`` does not exist (callers
    should treat "no skills available" as a normal state, not an error).
    """
    base = Path(skills_dir)
    if not base.is_dir():
        return {}
    discovered: dict[str, str] = {}
    for child in sorted(base.iterdir()):
        if not child.is_dir():
            continue
        manifest = child / SKILL_MANIFEST_FILENAME
        parsed = _parse_skill_manifest(manifest)
        if parsed is None:
            continue
        name, description = parsed
        discovered[name] = description
    return discovered


def _tokenize_for_skill_match(text: str) -> set[str]:
    """Lowercase, split on non-word characters, drop short stop-words.

    The recommender is intentionally simple: lexical overlap, no embeddings.
    Stop-words are dropped so generic words like "the" don't pull in every
    skill in the catalog. Two-character tokens are kept (``ci``, ``js``,
    ``go``) because they're often the most discriminating tokens.
    """
    stop = {
        "the",
        "and",
        "for",
        "with",
        "from",
        "that",
        "this",
        "into",
        "use",
        "uses",
        "using",
        "when",
        "what",
        "how",
        "to",
        "of",
        "a",
        "an",
        "in",
        "on",
        "by",
        "is",
        "be",
        "as",
        "at",
        "or",
        "it",
        "its",
        "you",
        "your",
        "we",
        "us",
        "our",
        "are",
        "was",
        "were",
        "has",
        "have",
        "had",
    }
    tokens: set[str] = set()
    for raw in re.split(r"\W+", text.lower()):
        if not raw or raw in stop:
            continue
        if len(raw) < 2:
            continue
        tokens.add(raw)
    return tokens


def recommend_skills_for_agent(
    description: str,
    available_skills: dict[str, str],
    limit: int = DEFAULT_SKILL_RECOMMENDATION_LIMIT,
) -> list[str]:
    """Rank ``available_skills`` by lexical overlap with ``description``.

    Args:
        description: The agent's purpose / task description.
        available_skills: ``{skill_name: skill_description}`` (typically
            from :func:`discover_available_skills`).
        limit: Maximum skills to return. Defaults to
            :data:`DEFAULT_SKILL_RECOMMENDATION_LIMIT`. ``limit <= 0``
            raises ``ValueError`` rather than silently returning an empty
            list — callers asking for "no skills" should not call this.

    Returns:
        Skill names in descending overlap order. Skills with zero overlap
        are excluded entirely (so an empty list means nothing matched,
        not that ``available_skills`` was empty). Ties are broken by
        skill name for deterministic output.
    """
    if limit <= 0:
        raise ValueError(f"recommend_skills_for_agent limit must be positive; got {limit}")
    if not description.strip() or not available_skills:
        return []
    desc_tokens = _tokenize_for_skill_match(description)
    if not desc_tokens:
        return []
    scored: list[tuple[int, str]] = []
    for name, skill_desc in available_skills.items():
        skill_tokens = _tokenize_for_skill_match(f"{name} {skill_desc}")
        overlap = len(desc_tokens & skill_tokens)
        if overlap > 0:
            scored.append((overlap, name))
    # Sort by descending score, then ascending name (stable, deterministic).
    scored.sort(key=lambda pair: (-pair[0], pair[1]))
    return [name for _, name in scored[:limit]]


def validate_skills_exist(
    skills: list[str],
    skills_dir: Path | str = DEFAULT_SKILLS_DIR,
) -> list[str]:
    """Return a list of error messages for any skill in ``skills`` not on disk.

    Empty input → empty output (no errors). One error per missing skill,
    naming the skill so the message is actionable without further lookup.
    Returns an empty list when every skill resolves; callers can use
    ``not validate_skills_exist(...)`` as a boolean health check.
    """
    if not skills:
        return []
    available = discover_available_skills(skills_dir)
    errors: list[str] = []
    for name in skills:
        if name not in available:
            errors.append(
                f"Skill '{name}' is not available under {skills_dir} "
                f"(no '{SKILL_MANIFEST_FILENAME}' found, or its frontmatter "
                "is missing 'name' / 'description')."
            )
    return errors


# Minimum number of sections an agent needs before a companion skill is worth
# generating. Simple single-purpose agents (just description + overview) don't
# benefit — the skill would duplicate the agent's own content. The threshold
# matches the "medium complexity" tier in the feature pipeline: multi-phase
# workflows, orchestrators, and agents with examples / prerequisites / error
# handling are where domain knowledge worth extracting lives.
COMPANION_SKILL_MIN_SECTIONS: int = 3

# Companion-skill filename is the Claude Code skill convention; reused from
# the discovery helpers so the two stay in lock-step — if the convention
# ever changes, both discover_available_skills and write_companion_skill
# pick it up from a single constant.
COMPANION_SKILL_FILENAME: str = SKILL_MANIFEST_FILENAME


def should_generate_companion_skill(definition: AgentDefinition) -> bool:
    """Decide whether ``definition`` is complex enough to warrant a companion skill.

    An agent gets a companion skill when **any** of these are true:

    - It has ≥ ``COMPANION_SKILL_MIN_SECTIONS`` sections (multi-phase
      workflows, prerequisites, examples, error handling — all of which
      benefit from a separate reference doc)
    - It uses an orchestrator / hierarchical pattern (has workers)
    - It uses a prompt-chaining pattern (has chain_steps)

    The predicate is deliberately conservative: false positives pollute
    the skills catalog with near-empty SKILL.md files, so we err toward
    "only generate when the agent actually has knowledge worth hoisting
    into a reference."
    """
    if len(definition.sections) >= COMPANION_SKILL_MIN_SECTIONS:
        return True
    if definition.workers:
        return True
    if definition.chain_steps:
        return True
    return False


def generate_companion_skill_content(definition: AgentDefinition) -> str:
    """Build the SKILL.md file body (frontmatter + domain knowledge sections).

    The generated skill captures agent-specific knowledge the agent will
    reference at runtime via its ``skills:`` frontmatter. Sections emitted:

    1. Frontmatter: ``name``, ``description``, ``allowed-tools``
    2. Title + description
    3. Workflow overview (if the agent has sections / workers / chain steps)
    4. Related agent reference (so the skill can be found from the agent)

    Returns a complete SKILL.md string ready to be written to disk.
    """
    allowed_tools = "\n".join(f"  - {tool}" for tool in definition.tools) or "  - Read"
    # Build a short workflow summary so the skill is self-contained rather
    # than a pointer back to the agent. If the agent has no workflow
    # material, fall back to the description — better terse than empty.
    workflow_lines: list[str] = []
    if definition.chain_steps:
        for idx, step in enumerate(definition.chain_steps, start=1):
            name = getattr(step, "name", f"step-{idx}")
            step_desc = getattr(step, "description", "")
            workflow_lines.append(f"{idx}. **{name}** — {step_desc}".rstrip(" —"))
    elif definition.workers:
        for worker in definition.workers:
            worker_role = getattr(worker, "role", "")
            workflow_lines.append(f"- **{worker.name}** — {worker_role}".rstrip(" —"))
    elif definition.sections:
        for section in definition.sections:
            title = getattr(section, "title", "")
            if title:
                workflow_lines.append(f"- {title}")

    workflow_body = "\n".join(workflow_lines) if workflow_lines else definition.description

    return (
        "---\n"
        f"name: {definition.name}\n"
        f"description: {definition.description}\n"
        "allowed-tools:\n"
        f"{allowed_tools}\n"
        "---\n"
        "\n"
        f"# {definition.name}\n"
        "\n"
        "## Overview\n"
        "\n"
        f"{definition.description}\n"
        "\n"
        "## Workflow\n"
        "\n"
        f"{workflow_body}\n"
        "\n"
        "## Related agent\n"
        "\n"
        f"This skill is the companion reference for the `{definition.name}` agent. "
        "Agents reference it via their `skills:` frontmatter field so the "
        "domain knowledge lives alongside the agent without bloating the "
        "agent's own prompt.\n"
    )


def write_companion_skill(
    definition: AgentDefinition,
    skills_dir: Path | str = DEFAULT_SKILLS_DIR,
    force: bool = False,
) -> Path | None:
    """Generate and write a companion SKILL.md for ``definition`` when warranted.

    Args:
        definition: The agent being generated. Its ``name`` is used as the
            skill directory name.
        skills_dir: Base directory for project skills. Defaults to
            :data:`DEFAULT_SKILLS_DIR`.
        force: When ``True``, skip the :func:`should_generate_companion_skill`
            predicate and always write. Useful for tests and for callers
            that have already made the decision externally.

    Returns:
        The written :class:`~pathlib.Path` when the companion skill was
        generated, ``None`` when :func:`should_generate_companion_skill`
        returned False (and ``force=False``).

    Side effects:
        - Creates ``<skills_dir>/<definition.name>/`` (parents included).
        - Writes SKILL.md (overwrites if present — callers with concerns
          about overwriting should check the path before calling).
        - Appends ``definition.name`` to ``definition.skills`` if not
          already present, so the agent's frontmatter references the
          companion skill.
    """
    if not force and not should_generate_companion_skill(definition):
        return None

    base = Path(skills_dir)
    skill_dir = base / definition.name
    skill_dir.mkdir(parents=True, exist_ok=True)
    path = skill_dir / COMPANION_SKILL_FILENAME
    path.write_text(generate_companion_skill_content(definition), encoding="utf-8")

    # Wire the new skill into the agent's frontmatter so the generated
    # agent actually references the file we just wrote. Idempotent —
    # repeat calls do not duplicate the entry.
    if definition.name not in definition.skills:
        definition.skills.append(definition.name)

    return path


